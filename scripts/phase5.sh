## 1. 핵심 가설

### 가설 1: 구조화 효율성
> "자유텍스트 진료 기록을 AI로 SOAP 구조화하면, 
> 의사의 문서화 시간이 수동 정리 대비 **40% 이상 단축**될 것이다."

- **독립변수**: 구조화 방식 (수동 vs AI 지원)
- **종속변수**: Task Completion Time (분)
- **통제변수**: 진료 기록 길이, 복잡도, 참가자 경력

### 가설 2: 구조화 정확도
> "AI가 생성한 SOAP 분류 결과의 정확도가 **85% 이상**일 것이다."

- **측정**: 전문가(의사) 리뷰 기반 항목별 정/오 판정
- **기준**: 각 문장이 올바른 SOAP 섹션에 배치되었는지

### 가설 3: 사용성
> "SnapSOAP의 SUS(System Usability Scale) 점수가 **70점 이상**일 것이다."

- SUS 70점 = "Good" 등급 (상위 ~30%)
- 목표 사용자(의사, PA, NP) 5명 이상 대상 측정

## 2. 성공 지표 (KPI)

| 지표 | 정의 | 목표값 | 측정 방법 | 측정 시점 |
|------|------|--------|-----------|-----------|
| **SOAP 분류 정확도** | AI가 올바른 SOAP 섹션에 배치한 비율 | ≥ 85% | 전문가 리뷰 (10건) | 프로토타입 완성 후 |
| **Task Completion Time** | 입력→구조화→확인 완료까지 소요 시간 | ≤ 수동 대비 60% | 스톱워치 측정 | 사용성 테스트 시 |
| **API 응답 시간** | 구조화 버튼 클릭→결과 표시 | ≤ 5초 (p95) | 서버 로그 | 실시간 |
| **SUS 점수** | System Usability Scale | ≥ 70점 | SUS 설문 10문항 | 사용성 테스트 후 |
| **Task Completion Rate** | 시나리오 테스트 완료율 | ≥ 90% | 시나리오 관찰 | 사용성 테스트 시 |
| **수정률** | AI 결과를 사용자가 수정한 비율 | ≤ 20% | 편집 로그 | 사용성 테스트 시 |

## 3. 검증 방법

### 3.1 정확도 검증 (Accuracy Test)

**방법**: 10건의 다양한 진료 기록으로 AI 구조화 실행 후, 
의료 전문가 2인이 독립적으로 정확도 평가

**평가 기준**:
- ✅ Correct: 문장이 올바른 SOAP 섹션에 배치됨
- ⚠️ Acceptable: 약간 모호하나 합리적 분류
- ❌ Incorrect: 명백한 오분류

**정확도 산출**: (Correct + Acceptable) / Total × 100

**테스트 케이스 구성**:
| # | 진료과 | 복잡도 | 주요 특징 |
|---|--------|--------|-----------|
| 1 | 내과 | 중 | 만성질환 f/u, 검사 결과 다수 |
| 2 | 응급 | 고 | 급성 증상, 빠른 의사결정 |
| 3 | 소아과 | 중 | 보호자 진술, 감염 |
| 4 | 정형외과 | 저 | 단순 외상 |
| 5 | 산부인과 | 중 | 정기 검진 |
| 6 | 내과 | 고 | 다장기 문제, 영어/한국어 혼용 |
| 7 | 정신과 | 중 | 주관적 호소 비중 높음 |
| 8 | 외과 | 중 | 수술 계획 포함 |
| 9 | 내과 | 저 | 간단한 감기 |
| 10 | 응급 | 고 | 다발성 외상 |

### 3.2 사용성 테스트 (Usability Test)

**참가자**: 의사 또는 의료진 3~5명 (모집 가능 범위 내)
**시간**: 참가자당 20~30분

**시나리오 테스트 (Task-based)**:
1. SnapSOAP에 접속하여 인터페이스를 파악하세요 (2분)
2. 샘플 진료 기록 하나를 선택하여 구조화를 실행하세요 (1분)
3. 결과에서 "Assessment" 섹션의 내용을 수정하세요 (2분)
4. 직접 진료 기록을 입력하고 구조화하세요 (5분)
5. 구조화된 결과를 복사하세요 (1분)

**측정 항목**:
- 각 태스크 완료 여부 (Success/Fail)
- 각 태스크 소요 시간
- 오류 발생 횟수
- SUS 설문 (10문항, 태스크 완료 후)
- 자유 피드백 (Think-aloud)

### 3.3 A/B 비교 (시간 검증)

**방법**: 동일한 진료 기록 3건에 대해
- (A) 수동 SOAP 정리: 참가자가 직접 텍스트를 읽고 SOAP으로 분류
- (B) SnapSOAP 사용: AI 구조화 후 결과 확인/수정

**측정**: 각 방식의 소요 시간, 최종 결과 품질

## 4. 검증 일정

| 주차 | 활동 | 산출물 |
